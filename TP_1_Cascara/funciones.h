/** \brief Pide al usuario ingresar un numero entero
 *
 * \return Numero entero
 *
 */
int ingresaNumero();


/** \brief Suma dos numeros enteros y los muestra
 *
 * \param Numero entero
 * \param Numero entero
 */
void suma(int,int);


/** \brief Resta dos numeros enteros y los muestra
 *
 * \param Numero entero
 * \param Numero entero
 */
void resta(int,int);


/** \brief Divide dos numeros enteros y los muestra, verifica que el divisor sea distinto de cero.
 *
 * \param Numero entero
 * \param Numero entero
 */
void division(int,int);


/** \brief Multiplica dos numeros enteros y los muestra
 *
 * \param Numero entero
 * \param Numero entero
 */
void multiplicacion(int,int);


/** \brief Calcula el factorial de un numero
 *
 * \param Numero entero
 */
void factorial(int);
